package ezhun.smsb;

/**
 * Plain old java object for Sender.
 */
public class SenderPojo {
	private String id;
	private String sender;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSender() {
		return sender;
	}

	public void setSender(String sender) {
		this.sender = sender;
	}
}
